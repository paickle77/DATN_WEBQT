import React, { useState, useEffect } from 'react';
import './NotificationManagement.scss';
import TabBar from '../../component/tabbar/TabBar';
import api from '../../utils/api';
import { toast } from 'react-toastify';

const NotificationManagement = () => {
  const [notes, setNotes] = useState([]);
  const [users, setUsers] = useState([]);
  const [stats, setStats] = useState({});
  const [loading, setLoading] = useState(false);
  
  // Form states
  const [sendType, setSendType] = useState('personal'); // 'personal' ho·∫∑c 'broadcast'
  const [selectedUserId, setSelectedUserId] = useState('');
  const [content, setContent] = useState('');
  
  // Filter states
  const [filterRead, setFilterRead] = useState('all');
  const [filterType, setFilterType] = useState('all');
  const [searchContent, setSearchContent] = useState('');
  
  // Bulk selection
  const [selectedIds, setSelectedIds] = useState([]);

  useEffect(() => {
    loadData();
  }, []);

  const loadData = async () => {
    setLoading(true);
    try {
      // üî• S·ª≠ d·ª•ng endpoints ri√™ng cho web admin
      const [notesRes, usersRes, statsRes] = await Promise.all([
        api.get('/notifications/admin/all'), // Endpoint ri√™ng cho web admin
        api.get('/users'),
        api.get('/notifications/admin/stats') // Endpoint ri√™ng cho th·ªëng k√™
      ]);
      
      setNotes(notesRes.data.data || []);
      // Ch·ªâ l·∫•y user ch∆∞a b·ªã kh√≥a
      setUsers((usersRes.data.data || []).filter(u => !u.is_lock));
      setStats(statsRes.data.data || {});
    } catch (error) {
      console.error('Load data error:', error);
      toast.error('Kh√¥ng th·ªÉ t·∫£i d·ªØ li·ªáu');
    } finally {
      setLoading(false);
    }
  };

  const handleSendNotification = async (e) => {
    e.preventDefault();
    
    if (!content.trim()) {
      toast.error('Vui l√≤ng nh·∫≠p n·ªôi dung th√¥ng b√°o');
      return;
    }

    if (content.length > 500) {
      toast.error('N·ªôi dung kh√¥ng ƒë∆∞·ª£c qu√° 500 k√Ω t·ª±');
      return;
    }

    if (sendType === 'personal' && !selectedUserId) {
      toast.error('Vui l√≤ng ch·ªçn ng∆∞·ªùi nh·∫≠n');
      return;
    }

    setLoading(true);
    try {
      let response;
      
      if (sendType === 'broadcast') {
        // G·ª≠i broadcast - s·ª≠ d·ª•ng endpoint ri√™ng cho admin
        response = await api.post('/notifications/admin/broadcast', { 
          content: content.trim() 
        });
        toast.success(response.data.msg);
      } else {
        // G·ª≠i c√° nh√¢n
        response = await api.post('/notifications', {
          user_id: selectedUserId,
          content: content.trim(),
          type: 'personal'
        });
        toast.success('ƒê√£ g·ª≠i th√¥ng b√°o c√° nh√¢n');
      }
      
      // Reset form v√† reload data
      setContent('');
      setSelectedUserId('');
      await loadData();
      
    } catch (error) {
      console.error('Send notification error:', error);
      toast.error(error.response?.data?.msg || 'G·ª≠i th√¥ng b√°o th·∫•t b·∫°i');
    } finally {
      setLoading(false);
    }
  };

  const handleMarkRead = async (id) => {
    try {
      await api.put(`/notifications/${id}`, { is_read: true });
      toast.success('ƒê√£ ƒë√°nh d·∫•u ƒë√£ ƒë·ªçc');
      loadData();
    } catch (error) {
      toast.error('Kh√¥ng th·ªÉ ƒë√°nh d·∫•u ƒë√£ ƒë·ªçc');
    }
  };

  const handleBulkMarkRead = async () => {
    if (selectedIds.length === 0) return;
    
    try {
      const response = await api.put('/notifications/admin/bulk/mark-read', {
        notification_ids: selectedIds
      });
      toast.success(response.data.msg);
      setSelectedIds([]);
      loadData();
    } catch (error) {
      toast.error('Kh√¥ng th·ªÉ ƒë√°nh d·∫•u ƒë√£ ƒë·ªçc');
    }
  };

  const handleDelete = async (id) => {
    if (!window.confirm('B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a th√¥ng b√°o n√†y?')) {
      return;
    }
    
    try {
      await api.delete(`/notifications/${id}`);
      toast.success('ƒê√£ x√≥a th√¥ng b√°o');
      loadData();
    } catch (error) {
      toast.error('Kh√¥ng th·ªÉ x√≥a th√¥ng b√°o');
    }
  };

  const handleBulkDelete = async () => {
    if (selectedIds.length === 0) return;
    
    if (!window.confirm(`B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a ${selectedIds.length} th√¥ng b√°o ƒë√£ ch·ªçn?`)) {
      return;
    }
    
    try {
      const response = await api.delete('/notifications/admin/bulk/delete', {
        data: { notification_ids: selectedIds }
      });
      toast.success(response.data.msg);
      setSelectedIds([]);
      loadData();
    } catch (error) {
      toast.error('Kh√¥ng th·ªÉ x√≥a th√¥ng b√°o');
    }
  };

  const handleSelectAll = (checked) => {
    if (checked) {
      setSelectedIds(filteredNotes.map(n => n._id));
    } else {
      setSelectedIds([]);
    }
  };

  const handleSelectOne = (id, checked) => {
    if (checked) {
      setSelectedIds(prev => [...prev, id]);
    } else {
      setSelectedIds(prev => prev.filter(x => x !== id));
    }
  };

  // Filter logic
  const filteredNotes = notes.filter(n => {
    // Filter by read status
    if (filterRead === 'read' && !n.is_read) return false;
    if (filterRead === 'unread' && n.is_read) return false;
    
    // Filter by type
    if (filterType === 'global' && n.type !== 'global') return false;
    if (filterType === 'personal' && n.type !== 'personal') return false;
    
    // Filter by search content
    if (searchContent && !n.content.toLowerCase().includes(searchContent.toLowerCase())) {
      return false;
    }
    
    return true;
  });

  const formatDateTime = (dateString) => {
    return new Date(dateString).toLocaleString('vi-VN', { 
      hour12: false,
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit'
    });
  };

  if (loading) {
    return (
      <div className="notification-management">
        <TabBar />
        <div className="loading-container">
          <div className="loading-spinner"></div>
          <p>ƒêang t·∫£i d·ªØ li·ªáu...</p>
        </div>
      </div>
    );
  }

  return (
    <div className="notification-management">
      <TabBar />
      
      <div className="header-section">
        <h2>Qu·∫£n l√Ω th√¥ng b√°o</h2>
        
        {/* Statistics Cards */}
        <div className="stats-cards">
          <div className="stat-card total">
            <div className="stat-icon">üìä</div>
            <div className="stat-info">
              <div className="stat-number">{stats.total || 0}</div>
              <div className="stat-label">T·ªïng th√¥ng b√°o</div>
            </div>
          </div>
          
          <div className="stat-card unread">
            <div className="stat-icon">üîî</div>
            <div className="stat-info">
              <div className="stat-number">{stats.unread || 0}</div>
              <div className="stat-label">Ch∆∞a ƒë·ªçc</div>
            </div>
          </div>
          
          <div className="stat-card global">
            <div className="stat-icon">üåê</div>
            <div className="stat-info">
              <div className="stat-number">{stats.global || 0}</div>
              <div className="stat-label">Th√¥ng b√°o chung</div>
            </div>
          </div>
          
          <div className="stat-card personal">
            <div className="stat-icon">üë§</div>
            <div className="stat-info">
              <div className="stat-number">{stats.personal || 0}</div>
              <div className="stat-label">Th√¥ng b√°o c√° nh√¢n</div>
            </div>
          </div>
        </div>
      </div>

      {/* Send Notification Form */}
      <form className="notify-form" onSubmit={handleSendNotification}>
        <div className="form-header">
          <h3>üì® G·ª≠i th√¥ng b√°o m·ªõi</h3>
        </div>
        
        <div className="form-content">
          {/* Send Type Selection */}
          <div className="send-type-selection">
            <div className="radio-group">
              <label className={`radio-option ${sendType === 'personal' ? 'active' : ''}`}>
                <input
                  type="radio"
                  value="personal"
                  checked={sendType === 'personal'}
                  onChange={(e) => setSendType(e.target.value)}
                />
                <span className="radio-custom"></span>
                <div className="option-content">
                  <span className="option-icon">üë§</span>
                  <span className="option-text">G·ª≠i c√° nh√¢n</span>
                </div>
              </label>
              
              <label className={`radio-option ${sendType === 'broadcast' ? 'active' : ''}`}>
                <input
                  type="radio"
                  value="broadcast"
                  checked={sendType === 'broadcast'}
                  onChange={(e) => setSendType(e.target.value)}
                />
                <span className="radio-custom"></span>
                <div className="option-content">
                  <span className="option-icon">üåê</span>
                  <span className="option-text">G·ª≠i t·∫•t c·∫£ ({users.length} ng∆∞·ªùi d√πng)</span>
                </div>
              </label>
            </div>
          </div>

          <div className="form-row">
            {/* User Selection - ch·ªâ hi·ªán khi g·ª≠i c√° nh√¢n */}
            {sendType === 'personal' && (
              <div className="input-group">
                <label>üë• Ch·ªçn ng∆∞·ªùi nh·∫≠n</label>
                <select 
                  value={selectedUserId} 
                  onChange={(e) => setSelectedUserId(e.target.value)}
                  required
                >
                  <option value="">-- Ch·ªçn ng∆∞·ªùi nh·∫≠n --</option>
                  {users.map(user => (
                    <option key={user._id} value={user._id}>
                      {user.name || user.email || `User ${user._id}`}
                      {user.phone && ` - ${user.phone}`}
                    </option>
                  ))}
                </select>
              </div>
            )}
            
            <div className="input-group flex-grow">
              <label>
                üí¨ N·ªôi dung th√¥ng b√°o 
                <span className="char-count">({content.length}/500)</span>
              </label>
              <textarea
                placeholder={sendType === 'broadcast' 
                  ? `Nh·∫≠p th√¥ng b√°o s·∫Ω g·ª≠i ƒë·∫øn t·∫•t c·∫£ ${users.length} ng∆∞·ªùi d√πng...`
                  : "Nh·∫≠p n·ªôi dung th√¥ng b√°o c√° nh√¢n..."
                }
                value={content}
                onChange={(e) => setContent(e.target.value)}
                maxLength={500}
                rows={3}
                required
              />
            </div>
            
            <div className="submit-section">
              <button type="submit" className="submit-btn" disabled={loading}>
                {loading ? (
                  <>‚è≥ ƒêang g·ª≠i...</>
                ) : (
                  <>üöÄ {sendType === 'broadcast' ? 'G·ª≠i t·∫•t c·∫£' : 'G·ª≠i ngay'}</>
                )}
              </button>
            </div>
          </div>
        </div>
      </form>

      {/* Filter Bar */}
      <div className="filter-bar">
        <div className="filter-section">
          <div className="search-container">
            <div className="search-icon">üîç</div>
            <input 
              placeholder="T√¨m ki·∫øm n·ªôi dung th√¥ng b√°o..." 
              value={searchContent}
              onChange={(e) => setSearchContent(e.target.value)} 
            />
          </div>
          
          <select value={filterRead} onChange={(e) => setFilterRead(e.target.value)}>
            <option value="all">üìã T·∫•t c·∫£ tr·∫°ng th√°i</option>
            <option value="unread">üîî Ch∆∞a ƒë·ªçc ({stats.unread || 0})</option>
            <option value="read">‚úÖ ƒê√£ ƒë·ªçc ({stats.read || 0})</option>
          </select>

          <select value={filterType} onChange={(e) => setFilterType(e.target.value)}>
            <option value="all">üìÇ T·∫•t c·∫£ lo·∫°i</option>
            <option value="global">üåê Th√¥ng b√°o chung</option>
            <option value="personal">üë§ Th√¥ng b√°o c√° nh√¢n</option>
          </select>
        </div>
        
        {selectedIds.length > 0 && (
          <div className="bulk-actions">
            <button onClick={handleBulkMarkRead} disabled={loading}>
              ‚úÖ ƒê√°nh d·∫•u ƒë√£ ƒë·ªçc
              <span className="count-badge">{selectedIds.length}</span>
            </button>
            <button onClick={handleBulkDelete} disabled={loading}>
              üóëÔ∏è X√≥a
              <span className="count-badge">{selectedIds.length}</span>
            </button>
          </div>
        )}
      </div>

      {/* Notifications Table */}
      {filteredNotes.length > 0 ? (
        <div className="table-container">
          <table className="notify-table">
            <thead>
              <tr>
                <th className="checkbox-col">
                  <input 
                    type="checkbox"
                    onChange={(e) => handleSelectAll(e.target.checked)}
                    checked={selectedIds.length === filteredNotes.length && filteredNotes.length > 0}
                    indeterminate={selectedIds.length > 0 && selectedIds.length < filteredNotes.length}
                  />
                </th>
                <th>üìä STT</th>
                <th>üë§ Ng∆∞·ªùi nh·∫≠n</th>
                <th>üí¨ N·ªôi dung</th>
                <th>üìÇ Lo·∫°i</th>
                <th>üìÖ Th·ªùi gian</th>
                <th>üìñ Tr·∫°ng th√°i</th>
                <th>‚öôÔ∏è H√†nh ƒë·ªông</th>
              </tr>
            </thead>
            <tbody>
              {filteredNotes.map((notification, index) => (
                <tr 
                  key={notification._id} 
                  className={`${notification.is_read ? 'read' : 'unread'} ${notification.type}`}
                >
                  <td className="checkbox-col">
                    <input 
                      type="checkbox"
                      checked={selectedIds.includes(notification._id)}
                      onChange={(e) => handleSelectOne(notification._id, e.target.checked)}
                    />
                  </td>
                  <td className="index-col">{index + 1}</td>
                  <td className="user-info">
                    <div className="user-details">
                      <div className="user-name">
                        {notification.user_id?.name || 
                         notification.user_id?.email || 
                         'Ng∆∞·ªùi d√πng kh√¥ng x√°c ƒë·ªãnh'}
                      </div>
                      {notification.user_id?.email && notification.user_id?.name && (
                        <div className="user-email">{notification.user_id.email}</div>
                      )}
                    </div>
                  </td>
                  <td className="content-col">
                    <div className="content-text" title={notification.content}>
                      {notification.content}
                    </div>
                    {notification.title && notification.title !== notification.content && (
                      <div className="content-title">üìå {notification.title}</div>
                    )}
                  </td>
                  <td className="type-col">
                    <span className={`type-badge ${notification.type}`}>
                      {notification.type === 'global' ? (
                        <>üåê Chung</>
                      ) : (
                        <>üë§ C√° nh√¢n</>
                      )}
                    </span>
                  </td>
                  <td className="date-col">
                    {formatDateTime(notification.created_at)}
                  </td>
                  <td className="status-col">
                    <span className={`status-indicator ${notification.is_read ? 'read' : 'unread'}`}>
                      {notification.is_read ? (
                        <>‚úÖ ƒê√£ ƒë·ªçc</>
                      ) : (
                        <>üîî Ch∆∞a ƒë·ªçc</>
                      )}
                    </span>
                  </td>
                  <td className="action-col">
                    <div className="action-buttons">
                      {!notification.is_read && (
                        <button 
                          className="mark-read-btn" 
                          onClick={() => handleMarkRead(notification._id)}
                          title="ƒê√°nh d·∫•u ƒë√£ ƒë·ªçc"
                        >
                          ‚úÖ
                        </button>
                      )}
                      <button 
                        className="delete-btn" 
                        onClick={() => handleDelete(notification._id)}
                        title="X√≥a th√¥ng b√°o"
                      >
                        üóëÔ∏è
                      </button>
                    </div>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      ) : (
        <div className="empty-state">
          <div className="empty-icon">
            {searchContent || filterRead !== 'all' || filterType !== 'all' ? 'üîç' : 'üì≠'}
          </div>
          <div className="empty-text">
            {searchContent || filterRead !== 'all' || filterType !== 'all' 
              ? 'Kh√¥ng t√¨m th·∫•y th√¥ng b√°o ph√π h·ª£p v·ªõi b·ªô l·ªçc'
              : 'Ch∆∞a c√≥ th√¥ng b√°o n√†o'
            }
          </div>
          {(searchContent || filterRead !== 'all' || filterType !== 'all') && (
            <button 
              className="clear-filters-btn"
              onClick={() => {
                setSearchContent('');
                setFilterRead('all');
                setFilterType('all');
              }}
            >
              üóëÔ∏è X√≥a b·ªô l·ªçc
            </button>
          )}
        </div>
      )}
    </div>
  );
};

export default NotificationManagement;